uint32_t testSpeed()
{
	int loop_count = 10000;
	initLoop();

	uint32_t start = HAL_GetTick();
	for(int i=0; i<loop_count; i++)
		speedLoop();
	uint32_t end = HAL_GetTick();

	return end-start;
}

